points = [(-1, 1), (5, 6), (12, 0),
          (4, 3), (0, 1), (-3, 2),
          (0, 0), (-1, 3), (2, 0),
          (3, 0), (-9, 1), (3, 6), 
          (8, 8)
          ]

# OA = (x**2 + y**2)**0.5 - расстояние от x0, y0 до точки
def comparator(item):  # ф-ия считает среднее ареф-е в кортеже
    return (item[0] ** 2 + item[1]**2)**0.5


print(sorted(points, key=comparator))
